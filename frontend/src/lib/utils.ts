export function getTemperatureColor(temp: number) {
  if (temp < 50) return 'text-blue-600' // Cold
  if (temp < 180) return 'text-yellow-600' // Warm
  return 'text-red-600' // Hot
}

export function formatPercentage(value: number, total: number) {
  if (total === 0) return '0%'
  return `${Math.round((value / total) * 100)}%`
}
